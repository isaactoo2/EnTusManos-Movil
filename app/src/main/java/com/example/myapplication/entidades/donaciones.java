package com.example.myapplication.entidades;

public class donaciones {
    private String idDonacion, tituloDonacion, cuerpoDonacion, userDonacion, photoDonacion, userUbicacion, idUserDonacion, userPhoto, fecha;

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getUserPhoto() {
        return userPhoto;
    }

    public void setUserPhoto(String userPhoto) {
        this.userPhoto = userPhoto;
    }

    public String getIdUserDonacion() {
        return idUserDonacion;
    }

    public void setIdUserDonacion(String idUserDonacion) {
        this.idUserDonacion = idUserDonacion;
    }

    public String getUserUbicacion() {
        return userUbicacion;
    }

    public void setUserUbicacion(String userUbicacion) {
        this.userUbicacion = userUbicacion;
    }

    public String getPhotoDonacion() {
        return photoDonacion;
    }

    public void setPhotoDonacion(String photoDonacion) {
        this.photoDonacion = photoDonacion;
    }

    public String getIdDonacion() {
        return idDonacion;
    }

    public void setIdDonacion(String idDonacion) {
        this.idDonacion = idDonacion;
    }

    public String getTituloDonacion() {
        return tituloDonacion;
    }

    public void setTituloDonacion(String tituloDonacion) {
        this.tituloDonacion = tituloDonacion;
    }

    public String getCuerpoDonacion() {
        return cuerpoDonacion;
    }

    public void setCuerpoDonacion(String cuerpoDonacion) {
        this.cuerpoDonacion = cuerpoDonacion;
    }

    public String getUserDonacion() {
        return userDonacion;
    }

    public void setUserDonacion(String userDonacion) {
        this.userDonacion = userDonacion;
    }
}
